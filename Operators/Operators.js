// Toán tử số học (Arithmetic Operators):
let a=5;let b=6;
console.log(a+b);
console.log(a*b);
//Toán tử gán (Assignment Operators):
let x = 5;
x += 9;
console.log(x);
//Toán tử so sánh (Comparison Operators):
console.log(5 == "5");  //so sánh kiểu giá trị 
console.log(5 === "5"); // so sánh kiểu giá trị và dữ liệu
//Toán tử logic (Logical Operators):
let c = true, d = false;
console.log(c && d); //Và (AND)
console.log(c || d); // Hoặc (OR)
console.log(!c);//Phủ định (NOT)
//Toán tử điều kiện (Conditional Operator - Ternary):
let age = 16;
let status = (age >= 18) ? "Adult" : "Minor";// toán tử ba ngôi
console.log(status); 
